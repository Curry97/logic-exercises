for i in range(1, 6):
    print("Número:", i)


'''
Como funciona o range()
range(1, 6) gera os números: 1, 2, 3, 4, 5
Importante: O último número (6) não é incluído na sequência. 
O range sempre para antes do valor final especificado.
Você também pode usar range() de outras formas: range(5) gera 0 até 4, e range(10, 0, -1) 
gera uma contagem regressiva de 10 até 1.'''